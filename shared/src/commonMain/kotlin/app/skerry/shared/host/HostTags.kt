package app.skerry.shared.host

/**
 * Максимальная длина одного тега и максимальное число тегов на хост. Это не вопрос безопасности
 * (теги идут только в текст Compose / JSON / сравнение строк — инъекций нет), а защита от
 * патологического ввода/вставки: гигантские или тысячами накопленные теги раздули бы `hosts.json`
 * и замедлили рендер/фильтрацию. Нормализация — единственный вход для тегов из формы.
 */
const val MAX_TAG_LENGTH = 32
const val MAX_TAGS_PER_HOST = 20

/**
 * Канонизировать тег хоста: trim, снять `#` с обоих концов, привести к нижнему регистру и обрезать
 * до [MAX_TAG_LENGTH]; пустой результат — `null` (тег не добавляется). Каноническая форма делает
 * фильтрацию по чипсам сравнением строк и исключает дубли «Prod»/«#prod». Живёт в `shared` (а не в
 * UI-слое), потому что [Host.tags] хранится в этой форме — любой путь записи хоста (форма, будущий
 * sync-импорт, миграция) должен проходить через одну и ту же канонизацию. Чистая функция —
 * зафиксирована [app.skerry.shared.host.HostTagsTest].
 */
fun normalizeTag(raw: String): String? =
    raw.trim().trim('#').trim().lowercase().take(MAX_TAG_LENGTH).ifBlank { null }
